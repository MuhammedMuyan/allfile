package com.neotech.lesson10;

public class NestedLoop {

	public static void main(String[] args) {
		
		
		// iterate a number 5 times
		
		
		for( int i = 0; i < 5; i++) {
			
			for(int j = 0; j<5; j++) {
				System.out.println("i: " + i + " j: " + j);
			}
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}

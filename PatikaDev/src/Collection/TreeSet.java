package Collection;

public class TreeSet {

	private String student;
	private int note;
	
	
	public TreeSet(String student, int note) {
		super();
		this.student = student;
		this.note = note;
	}


	public String getStudent() {
		return student;
	}


	public void setStudent(String student) {
		this.student = student;
	}


	public int getNote() {
		return note;
	}


	public void setNote(int note) {
		this.note = note;
	}
	
	
	
	
	
	
	
}
